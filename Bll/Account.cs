		public List<guanbingking.Model.Account> DataTableToList(DataTable dt)
		{
			List<guanbingking.Model.Account> modelList = new List<guanbingking.Model.Account>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				guanbingking.Model.Account model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

